package pt.contabilidade.util;

public class ContabilidadeException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ContabilidadeException() {
		super();
	}

	public ContabilidadeException(String message) {
		super(message);
	}

	public ContabilidadeException(String message, Throwable cause) {
		super(message, cause);
	}

	public ContabilidadeException(Throwable cause) {
		super(cause);
	}

}
